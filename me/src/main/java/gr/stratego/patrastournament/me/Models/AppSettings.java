package gr.stratego.patrastournament.me.Models;

public class AppSettings {


    private String accentColor;

    private String secondaryAccentColor;

    private String primaryColor;

    private String secondaryColor;

    private String darkTextColor;

    private String lightTextColor;

    private String resultsUrl;

    public String getAccentColor() {
        return accentColor;
    }

    public String getSecondaryAccentColor() {
        return secondaryAccentColor;
    }

    public String getPrimaryColor() {
        return primaryColor;
    }

    public String getSecondaryColor() {
        return secondaryColor;
    }

    public String getDarkTextColor() {
        return darkTextColor;
    }

    public String getLightTextColor() {
        return lightTextColor;
    }

    public String getResultsUrl() {
        return resultsUrl;
    }
}
